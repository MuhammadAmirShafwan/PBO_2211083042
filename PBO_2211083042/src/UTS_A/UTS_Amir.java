    public void diskon() {
        int A;
        int B;
        int C;
        int D;
        A = Integer.parseInt(txtMbi.getText());
        B = Integer.parseInt(txtMbl.getText());
        C = Integer.parseInt(txtTarif.getText());
        D = (int) (C * 97 / 100);
        if (A - B > 20) {
            txtTarif.setText(String.valueOf(D));
        } else {
            txtTarif.setText(String.valueOf(C));
        }
    }

