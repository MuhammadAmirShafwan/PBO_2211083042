    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        diskon();
        if (txtKode.getText().trim().equals("") || txtNama.getText().trim().equals("") || txtMbi.getText().trim().equals("") || txtMbl.getText().trim().equals("") || txtTarif.getText().trim().equals("") || txtDiskon.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Tidak boleh ada data yang kosong \n Input ulang");
        } else {
            model.addRow(new Object[]{txtKode.getText(), txtNama.getText(), txtMbi.getText(), txtMbl.getText(), txtTarif.getText(), txtDiskon.getText()});
            //JOptionPane.showMessageDialog(null, "Data berhasil di input");
        }
        reset();

    }//GEN-LAST:event_btnInsertActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        if (jTable1.getSelectedRow() == -1) {
            if (jTable1.getRowCount() == 0) {
                JOptionPane.showMessageDialog(null, "Tidak ada data dalam tabel");
            } else {
                JOptionPane.showMessageDialog(null, "Pilih data yang ingin diubah");
            }
        } else {
            if (txtKode.getText().trim().equals("") || txtNama.getText().trim().equals("") || txtMbi.getText().trim().equals("") || txtMbl.getText().trim().equals("") || txtTarif.getText().trim().equals("") || txtDiskon.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(null, "Tidak boleh ada data yang kosong \n Input ulang");
            } else {
                diskon();
                model.setValueAt(txtKode.getText(), jTable1.getSelectedRow(), 0);
                model.setValueAt(txtNama.getText(), jTable1.getSelectedRow(), 1);
                model.setValueAt(txtMbi.getText(), jTable1.getSelectedRow(), 2);
                model.setValueAt(txtMbl.getText(), jTable1.getSelectedRow(), 3);
                model.setValueAt(txtTarif.getText(), jTable1.getSelectedRow(), 4);
                model.setValueAt(txtDiskon.getText(), jTable1.getSelectedRow(), 5);
            }
        }
        reset();
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        if (jTable1.getSelectedRow() == -1) {
            if (jTable1.getRowCount() == 0) {
                JOptionPane.showMessageDialog(null, "Tidak ada data dalam tabel");
            } else {
                JOptionPane.showMessageDialog(null, "Pilih data yang ingin dihapus");
            }
        } else {
            model.removeRow(jTable1.getSelectedRow());
        }
        reset();
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        txtKode.setText(model.getValueAt(jTable1.getSelectedRow(), 0).toString());
        txtNama.setText(model.getValueAt(jTable1.getSelectedRow(), 1).toString());
        txtMbi.setText(model.getValueAt(jTable1.getSelectedRow(), 2).toString());
        txtMbl.setText(model.getValueAt(jTable1.getSelectedRow(), 3).toString());
        txtTarif.setText(model.getValueAt(jTable1.getSelectedRow(), 4).toString());
        txtDiskon.setText(model.getValueAt(jTable1.getSelectedRow(), 5).toString());
    }//GEN-LAST:event_jTable1MouseClicked

